package criaderoDeTruchas;

public class Estanque {
	int numeroEstanque;
	int capacidadMaxima;
	boolean tieneVertedero;
	int alturaTanque;
	int cantidadLitrosCargados;
	
	
	public Estanque(int numeroEstanque, int capacidadMaxima, int alturaTanque) {
		
		this.numeroEstanque = numeroEstanque;
		this.capacidadMaxima = capacidadMaxima;
		this.tieneVertedero = false;
		this.alturaTanque = alturaTanque;
	}
	
	

}
